namespace Chap3_PrintMax;

class PrintMax
{
    static void Main()
    {
        Console.WriteLine(short.MaxValue);    // short型の最大値
        Console.WriteLine(int.MaxValue);      // int型の最大値
        Console.WriteLine(long.MaxValue);     // long型の最大値
        Console.WriteLine(double.MaxValue);   // double型の最大値
        Console.WriteLine(decimal.MaxValue);  // decimal型の最大値
    }
}

